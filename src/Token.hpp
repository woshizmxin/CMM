#pragma once

#include <string>
#include "Token.h"
#include "TokenType.hpp"

namespace CMM
{

////////////////////////////////////////////////////////////////////////////////
// Using
////////////////////////////////////////////////////////////////////////////////

using std::string;


////////////////////////////////////////////////////////////////////////////////
// Constructor
////////////////////////////////////////////////////////////////////////////////

Token::Token(TokenType tokenType, const string &tokenStr, int lineNo):
    __tokenType(tokenType),
    __tokenStr (tokenStr),
    __lineNo   (lineNo) {}


////////////////////////////////////////////////////////////////////////////////
// Getter: __tokenType
////////////////////////////////////////////////////////////////////////////////

TokenType Token::tokenType() const
{
    return __tokenType;
}


////////////////////////////////////////////////////////////////////////////////
// Getter: __tokenStr
////////////////////////////////////////////////////////////////////////////////

string &Token::tokenStr()
{
    return __tokenStr;
}


const string &Token::tokenStr() const
{
    return __tokenStr;
}


////////////////////////////////////////////////////////////////////////////////
// Getter: __lineNo
////////////////////////////////////////////////////////////////////////////////

int Token::lineNo() const
{
    return __lineNo;
}


}  // End namespace CMM
